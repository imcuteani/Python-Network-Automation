# Python with SDN for Network Controller API 

# Tools and Frameworks for Python SDN library 

# NetworkX -> python package used for creation, manipulation and learn about the structure, dynamics and functions for networks. It's used for path manipulation and topology analysis in SDN. 

# POX -> open source python based SDN controller platform, used for prototyping of Openflow based network apps. 

# Ryu -> popular component based SDN framework, supports Openflow protocol, enabling to build custom controllers which actively manage traffic, acts as firewalls, dynamically route packets using scripts. 

# Mininet -> industry standard Python SDN emulator, allows to create virtual networks (hosts, switches, controllers) on a single linux server. 
# you can define custom topologies using the python api. 